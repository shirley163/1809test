<?php

// 字符串拼接运算符 .
//
// 将两部分内容,拼接为一个整体的字符串
//
// 拼接的过程中,会进行数据类型的自动转化
//
// 与小数点进行区别
//
//         拼接运算符前后有空格
//
//         小数点前后没有空格
//

$str = '北京';

$string = $str . '上海';

$string = $str . 100;

$string = $str . true;

$string = $str . false;

$string = 100 . 100;

$string = 100 . 100.001;

$string = 100 . 100 . 001;
